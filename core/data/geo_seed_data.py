def iter_us_geo_strings():
    seen = set()
    for st, capital in US_CAPITALS.items():
        if st == "DC":
            geo = "Washington, DC"
        else:
            geo = f"{capital}, {st}"
        if geo not in seen:
            seen.add(geo)
            yield geo
    for st, cities in US_MAJOR_CITIES.items():
        for city in cities:
            if st == "DC":
                geo = "Washington, DC"
            else:
                geo = f"{city}, {st}"
            if geo not in seen:
                seen.add(geo)
                yield geo


